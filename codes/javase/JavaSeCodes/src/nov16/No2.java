package nov16;

public class No2 {
	private String name;
	private int age;
	private double gpa;
	
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return name;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getAge() {
		return age;
	}
	public void setGpa(double gpa) {
		this.gpa = gpa;
	}
	public double getGpa() {
		return gpa;
	}
}
